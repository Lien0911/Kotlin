fun main(args:Array<String>){
    /*
    When
    */

    var colorCode=readLine()
    when(colorCode){
        "R","r"->{
            println("您選擇的是紅色")
        }
        "B","b"->{
            println("您選擇的是藍色")
        }
        "G","g"->{
            println("您選擇的是綠色")
        }
        else ->{
            println("看不出來您選擇的是哪種顏色")
        }
    }






}